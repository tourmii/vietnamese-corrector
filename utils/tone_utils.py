TONE_TO_VOWEL: dict[tuple[str, int], str] = {v: k for k, v in VOWEL_TONES.items()}


def strip_tone(char: str) -> tuple[str, int]:
    return VOWEL_TONES.get(char.lower(), (char.lower(), -1))


def apply_tone(base: str, tone: int, original_case: str) -> str:
    result = TONE_TO_VOWEL.get((base, tone), base)
    return result.upper() if original_case.isupper() else result


